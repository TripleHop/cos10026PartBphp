      <h2>The future of the technology.</h2>
      <p>MMORPG’s over the years have seen a constant growth in the gaming market. With the recent releases of new MMORPG’s the 
        market is predicted to grow consistently during the period of 2021-2026.<sup><a href="#fn3" id="ref3" title="Jump to footnote 3 in sources.">[3]</a></sup></p>
        <ol>
        <li>The growth of this game genre is being pushed by the accessibility of the internet around the world, predominantly in “developing economies 
          such as India and Brazil”.
        <li>Countries such as North America are predicted to have the fastest growing market due to the rise of people from the US and Canada 
          playing videogames.
        <li>With the global outbreak of COVID-19, the MMORPG market received positive growth due to increased use of the internet and people staying at home.</li>

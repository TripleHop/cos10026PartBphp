      <h2>Who developed it? When? Why?</h2>
      <p>The common term of MMORPG is widely known by gamers and is hard to miss if you have been in the gaming market for a few years. 
        Although variations of the game genre has existed for years, the first MMORPG was a game titles "Neverwinter Nights" by designer Don Daglow and programmer 
        Cathryn Mataga.<sup><a href="#fn1" id="ref1" title="Jump to footnote 1 in sources.">[1]</a></sup></p>
      </br>
      <h3>When?</h3>
      <p>Neverwinter Nights was released in 1991 and initiated one of the most prevalent gaming genres in today’s market.</p>
    </br>
      <h3>Why?</h3>
      <p>There isn’t a clear answer as to where the idea came from, Lead designer Don Daglow said ”These games are not just entertainment. 
        They build new kinds of communities and create deep people-to-people relationships. They influence people’s lives in the same way as great books, 
        inspiring movies and iconic television shows”.<sup><a href="#fn2" id="ref2" title="Jump to footnote 2 in sources.">[2]</a></sup></p>

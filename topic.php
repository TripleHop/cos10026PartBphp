      <h2>What are related genres?</h2>
      <p>There are many different kinds of game genres, popular genres of games include FPS, MOBA, 
        Sandbox, and RTS. While genres like RPG are very similar to MMORPG where the difference only exists in the multiplayer aspect, 
        other genres like FPS are completely different to MMORPG. MMORPG usually consists of a top down view, character progression system, 
        character customization system and typically a subscription service, whereas FPS are 3D shooter games in the first person perspective of the protagonist.</p>
        
      <p>FPS</p>
      <figure class="floatright">
        <img src="images/valorant.png" alt="val">
        <figcaption>Valorant: A Widely Popular FPS in the Market</figcaption>
      </figure>
      <ul>
        <li>Shooter</li>
        <li>In first person perspective</li>
        <li>Experiences the game through the eyes of the protagonist in a 3D space</li>
      </ul>
      <p>Platform</p>
      <ul>
        <li>Two-dimensional graphics</li>
        <li>Players control characters that usually jump through different platforms on the screen</li>
      </ul>
      <p>RTS</p>
      <ul>
        <li>Allows players to play simultaneously, in "real time"</li>
        <li>Usually create units and structures that are limited by expending resources</li>
      </ul>
      <p>TBS</p>
      <ul>
        <li>Turn based</li>
        <li>Also create units and structures that are limited by expending resources</li>
      </ul>
      <p>Puzzlers</p>
      <ul>
        <li>Expect to play a game based on a theme or traditional tabletop game</li>
        <li>Often include multiplayer elements</li>
      </ul>
